#include "libft.h"

int	ft_atoi(const char *nptr)
{
	long		res;
	int			sign;

	res = 0;
	sign = 1;
	while (*nptr == 32 || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			sign *= -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		res = res * 10 + *nptr - '0';
		if (res < 0 && sign > 0)
			return ((int)LONG_MAX);
		if (res < 0 && sign < 0)
			return ((int)LONG_MIN);
		nptr++;
	}
	return ((res * sign));
}
